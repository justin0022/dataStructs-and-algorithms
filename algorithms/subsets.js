// n = 4 => {}, {1}, {2}, {1, 2}, {3}, {1, 3}, {2, 3}, {1, 2, 3}, {4}, {1, 4}, {2, 4}, {3, 4}, {1, 2, 4}, {1, 3, 4}, {2, 3, 4}, {1, 2, 3, 4}

const getAllSubsets = n => {
  return [...Array(n).keys()].map(x => x + 1)
    .reduce((acc, cur) => [...acc, ...(acc.map(x => [...x, cur]))], [[]])
}

module.exports = getAllSubsets
